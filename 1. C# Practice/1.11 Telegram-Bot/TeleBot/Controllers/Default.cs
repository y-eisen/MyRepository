using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using Telegram.Bot;
using Telegram.Bot.Types;
using T_Bot.Configuration;


namespace T_Bot.Controllers
{
    public class DefaultControl
    {
        private readonly ITelegramBotClient _telegramClient;

        public DefaultControl(ITelegramBotClient telegramBotClient)
        {
            _telegramClient = telegramBotClient;
        }

        public async Task Handle(Message message, CancellationToken ct)
        {
            Console.WriteLine($"Контроллер {GetType().Name} получил сообщение");
            await _telegramClient.SendTextMessageAsync(message.Chat.Id, $"Получено непойми что", cancellationToken: ct);
        }

    }
}

